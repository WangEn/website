//点击时判断是否支付
function getPayStatus(e, fn){
    var imgId = $(e).attr('data-id');
    var amount = $(e).attr('data-fee');
    var obj = {
        phone_num: localStorage.getItem('username'),
        pid: imgId,
        amount: amount
    };
    $.ajax({
        url: apihost+'buy',
        type: "GET",
        dataType: "json",
        data: obj,
        success: function(res) {
          if (res.status == 0) {
            isPay = res.res.isBuy;
            if(isPay){
                //已支付
                fn(e);
                return true;
            }else{
                //未支付，跳转支付
                alert('请在支付完成后，再次点击下载~');
                $("body").html(res.res.payForm);
                return false;
            }
          } else {
            alert(res.msg);
            return false;
          }
        }
      });
}